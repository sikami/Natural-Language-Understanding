package Result;

public class Emotion {
    private String text;
    private String sadness;
    private String joy;
    private String fear;
    private String disgust;
    private String anger;

    public Emotion(String text, String sadness, String joy, String fear, String disgust, String anger) {
        this.text = text;
        this.sadness = sadness;
        this.joy = joy;
        this.fear = fear;
        this.disgust = disgust;
        this.anger = anger;
    }
}
